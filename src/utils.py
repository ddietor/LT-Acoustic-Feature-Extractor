def curveReaderPSDdataCum(csv_file, column='Mean'):
    """
    Lee PSDdata_cum.csv y devuelve frecuencia y la curva seleccionada.

    Parameters
    ----------
    csv_file : str
        Ruta al CSV.
    column : str
        'Mean', 'PCTL25', 'PCTL50', 'PCTL75', 'PCTL90' o 'PCTL95'.

    Returns
    -------
    freq : np.ndarray
        Frecuencias (Hz).
    psd : np.ndarray
        Valores de la columna seleccionada.
    """
    
    df = pd.read_csv(csv_file, sep=';')

    if column not in df.columns:
        raise ValueError(
            f"'{column}' no existe. Opciones: {list(df.columns[1:])}"
        )

    freq = df['Freq_Hz'].to_numpy()
    psd = df[column].to_numpy()

    return freq, psd

# %% low_pass_filter function():
def low_pass_filter(signal, cutoff_freq, sample_rate, order=4):
    """
    Apply a zero-phase Butterworth low-pass filter.

    Parameters
    ----------
    signal : ndarray
        Input signal.
    cutoff_freq : float
        Low-pass cutoff frequency [Hz].
    sample_rate : float
        Sampling frequency [Hz].
    order : int, optional
        Filter order.

    Returns
    -------
    ndarray
        Filtered signal.
    """
    nyquist = 0.5 * sample_rate
    normalized_cutoff = cutoff_freq / nyquist

    b, a = scp.signal.butter(
        order,
        normalized_cutoff,
        btype="low"
    )

    return scp.signal.filtfilt(b, a, signal)